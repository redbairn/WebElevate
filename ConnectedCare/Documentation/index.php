							<div class="col-lg-8">
								<p>The following concept has been put forward under the theme of a connected home.<p>
								<p>ConnectedCare - TechHome Care is a comprehensive healthcare monitor (a smart phone application linked with supporting technology and website) that enables the client to live independently with the added reassurance to both them and their relatives. The product aims to support the client across various systems and would be aimed at anyone living alone, but particularly the elderly or infirm.</p> 
								<p>ConnectedCare links a ‘fitbit’ (or similar device) that the client wears. This would be linked to the ConnectedCare portal and be used to track the client’s heart rate, blood pressure, movement, steps (pedometer) and sleeping pattern. Additional functions that will be added would track calorie intake and to link the 'fitbit' to an altimeter and home mapping technology (Movidius) that would locate the person in their environment in case of falls or serious incapacity. The aim would allow emergency responders to pinpoint current locations to speed support.</p> 
								<p>Connected into the system would be a ‘smart pillbox’. The wearable tool would remind clients on times to take medications and the ‘smart pillbox’ would log in when that dose was accessed. This would be added to the recorded information provided by the “fitbit’ to build a a picture of the clients actions.</p> 
								<p>Additional functionality can be added by extending the range of the monitoring to places such as a homes garden by covering such well used areas with Bluetooth connectivity.</p>
								<p>Another option ConnecetedCare can provide is to place NFC chips in a smartphone allowing tags to be placed around the house to ease certain tasks. A tag on photos of family members allows the phone, when the photo is tapped, to dial a person’s number. Tags could also be placed in certain locations throughout the client’s home a “Call Me” signal to family or emergency responders.</p>
								<p>ConnectedCare would monitor the client’s movement in the house, and alert the primary caregiver if sensors indicated the user might be unwell or have fallen.</p>
								<p>The client has control of who receives information provided although once set up if disconnected the distant receiver would receive an emergency notification.</p>
